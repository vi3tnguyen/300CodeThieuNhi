package Options1;

public class Cash {
    int in;
    int out;

    public int getIn() {
        return in;
    }

    public void setIn(int in) {
        this.in = in;
    }

    public int getOut() {
        return out;
    }

    public Cash(int in, int out) {
        this.in = in;
        this.out = out;
    }

    public void setOut(int out) {
        this.out = out;
    }
}
